package com.dj.day04.demo010;

public class While {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("我错了");
        }
        System.out.println("===========");
        int i = 0;
        while (i<10) {
            System.out.println("我错了");
            i++;
        }
    }
}
